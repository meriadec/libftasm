global _ft_isascii

section .text

_ft_isascii:
	push rbp
	mov rbp, rsp
	; core
	mov rax, 1
	cmp rdi, 0
	js nop
	cmp rdi, 128
	js yep

nop:
	mov rax, 0
	jmp end

yep:
	mov rax, 1
	jmp end

end:
	mov rsp, rbp
	pop rbp
	ret
